function getGuideRole(): GuideRole {
  const currentUser = getStoredUser();

  if (isAdminUser(currentUser)) {
    return 'admin';
  }

  if (isLearnerUser(currentUser)) {
    return 'employee';
  }

  return 'manager';
}

function getGuideTitle(role: GuideRole) {
  if (role === 'admin') return 'Руководство администратора';
  if (role === 'employee') return 'Руководство нового сотрудника';
  return 'Руководство менеджера';
}

function getGuideIntro(role: GuideRole) {
  if (role === 'admin') {
    return 'Этот раздел описывает работу с пользователями, ролями и списком сотрудников в системе MentorAI.';
  }

  if (role === 'employee') {
    return 'Этот раздел поможет новому сотруднику разобраться, как проходить обучение, тесты и тренировочные диалоги.';
  }

  return 'Этот раздел описывает работу руководителя или менеджера с документами, курсами, тестами, тренажером и аналитикой.';
}

function getGuideSections(role: GuideRole) {
  if (role === 'admin') return adminSections;
  if (role === 'employee') return employeeSections;
  return managerSections;
}

export function GuidePage() {
  const role = getGuideRole();
  const sections = getGuideSections(role);

  return (
    <div className="guide-page">
      <div className="guide-breadcrumbs">Главная &gt; Руководство пользователя</div>

      <section className="guide-hero">
        <div>
          <p className="guide-eyebrow"></p>
          <h1>{getGuideTitle(role)}</h1>
          <p>{getGuideIntro(role)}</p>
        </div>
      </section>

      <div className="guide-layout">
        <aside className="guide-toc">
          <h2>Содержание</h2>
          {sections.map((section, index) => (
            <a key={section.title} href={`#guide-section-${index + 1}`}>
              {section.title}
            </a>
          ))}
        </aside>

        <main className="guide-content">
          {sections.map((section, index) => (
            <section
              className="guide-card"
              id={`guide-section-${index + 1}`}
              key={section.title}
            >
              <h2>{section.title}</h2>
              <p>{section.text}</p>

              {section.steps && section.steps.length > 0 ? (
                <ol>
                  {section.steps.map((step) => (
                    <li key={step}>{step}</li>
                  ))}
                </ol>
              ) : null}

              {section.note ? (
                <div className="guide-note">
                  <strong>Важно:</strong> {section.note}
                </div>
              ) : null}
            </section>
          ))}
        </main>
      </div>
    </div>
  );
}
